my ($fl) = $ARGV[0];

open(PKGS, "<$fl") || die "Unable to open $fl";
my $buf = "";

while (my $line=<PKGS>) {
	chomp($line);
	$buf = "$line\n$buf";
}

close(PKGS);
print "$buf";
